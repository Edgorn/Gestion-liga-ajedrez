    private void iniciarFecha() {
        for (int i=0; i<100; i++)
            jComboBoxAnyo.addItem(anyo-i+"");
       
        totalDias();
    }
    
    private void totalDias() {
        int mes = jComboBoxMes.getSelectedIndex();
        int any = Integer.parseInt(""+jComboBoxAnyo.getSelectedItem());
        int totalDias = 31;
        
        switch (mes) {
            case 0:
            case 2:
            case 4:
            case 6:
            case 7:
            case 9:
            case 11:
                totalDias=31;
                break;
                
            case 3:
            case 5:
            case 8:
            case 10:
                totalDias=30;
                break;
                
            case 1:
                if (any%4==0) {
                    if(any%100==0) {
                        if(any%400==0) {
                            totalDias=29;
                        } else {
                            totalDias=28;
                        }
                    } else {
                        totalDias=29;
                    }
                } else {
                    totalDias=28;
                }
                break;
        }
        
        for (int i = 1; i<=totalDias; i++) {
            jComboBoxDia.addItem(i+"");
        }
    }
    
    private void llenarClubes() {
        ArrayList<String> clubes = liga.listaClubes();
        
        for (int i=0; i<clubes.size(); i++) {
            clubComboBox.addItem(clubes.get(i));
        }
    }

